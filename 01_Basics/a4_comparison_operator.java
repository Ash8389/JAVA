public class a4_comparison_operator {
    public static void main(String []args){
        int a = 10;
        int b = 20;

        boolean res = a<b;
        System.out.println(res);
        res = a>b;
        System.out.println(res);
        res = a==b;
        System.out.println(res);
        res = a<=b;
        System.out.println(res);
        res = a!=b;
        System.out.println(res);
    }
}
